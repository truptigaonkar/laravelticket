    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request,
            [
                'category' => 'required|unique:categories'
            ]
        );
        // Create category
        $categories = new Category();
        $categories->category = $request->input('category');
        $categories->save();
        return redirect('/categories')->with('success', 'Category Added Successfully!!!!'); //Create folder inc views/inc/messages.blade.php & adding '@include('inc.messages');' inside Layouts/app.blade.php    

    }

